#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	i;
	char			a;

	if (n < 0)
	{
		write(fd, "-", 1);
		i = -n;
	}
	else
		i = n;
	if (i > 9)
	{
		ft_putnbr_fd(i / 10, fd);
		i %= 10;
	}
	a = i + '0';
	write(fd, &a, 1);
}
